    public function store(Request $request)
    {
       
       if($request->hasFile('pdf')) {
        $request->file('pdf');
        //return $request->pdf->store('public'); This is one way of storing files another way is using Storage
        // another way to store giving it a particular name is by definning the name as in the example shown below
        //return Storage::putFile('public', $request->file('pdf'));
        return $request->pdf->storeAs('public', 'ulrich.pdf');
       } else {
        return 'No File Was Selected';
       }
       
    }

